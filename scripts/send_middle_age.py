def send_to_feishu(content):
    """发送到飞书"""
    url = WEBHOOK
    payload = {
        "msg_type": "text",
        "content": {
            "text": f"━━━━━━━━━━━━━━━━\n{content}\n━━━━━━━━━━━━━━━━\n📅 {datetime.now().strftime('%Y-%m-%d')} | 中年觉醒"
        }
    }

    response = requests.post(url, json=payload)
    result = response.json()

    if result.get("code") == 0:
        print("✅ 中年觉醒内容发送成功")
        return True
    else:
        print(f"❌ 发送失败: {result}")
        return False

